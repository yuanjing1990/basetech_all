public class ExciseOperator {
    public class Number {
        public int num;
    }
    public static void main(String[] args) {
        int a = 3, b = 4, c = 5;
        System.out.println("a = " + a + ", b = " + b + ", c = " + c);
        System.out.println(" a + b = " + (a + b));
        System.out.println(" a - b = " + (a - b));
        System.out.println(" a * b = " + (a * b));
        System.out.println(" a / b = " + (a / b));
        System.out.println(" a % b = " + (a % b));
    }
}