public class BitOp
{
	public static void main(String args[])
	{
		int a = 9;
		int b = 15;
		int c = 8;
		int d,e,f,g,h;
		d = a & b;
		e = a | b;
		f = a ^ b;
		g = a << 2;
		h = c >> 1;
		System.out.println("d="+d);
		System.out.println("e="+e);
		System.out.println("f="+f);
		System.out.println("g="+g);
		System.out.println("h="+h);
	}
}